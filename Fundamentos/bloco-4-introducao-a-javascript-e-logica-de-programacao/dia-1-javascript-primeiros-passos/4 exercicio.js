// Faça um programa que, dado um valor definido numa constante, retorne "positive" se esse valor for positivo, "negative" se for negativo e "zero" caso contrário.

const a = -10;

if (a > 0) {
    console.log("Positivo");
}
else if (a < 0) {
    console.log("Negativo");
}
else {
    console.log("zero");
}